#include <iostream>
using namespace std;

int main() {

  	// for loop to print "Hi" 5 times
    for (int i = 0; i < 5; i++) { //คำสั่งLoop โดยให้ i = 0 เช็คว่า i < 5 ไหมถ้าใช้Loop แล้วเพิ่ม i ทีละ1
      	cout << "Hi => i = " << i << endl; //แสดงค่าที่Terminal Hi => i = , ค่าของตัวแปล i
    }
  
    return 0;
}